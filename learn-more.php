				<h2>Recording Birdsong</h2>
				
				<p class="lead">Soon you won't be content using the recordings that others supply for you, and you will want to make your own. With modern technology, that undertaking is increasingly simple, and increasingly inexpensive.</p> 
				<p>Rather than provide my own guidelines for recording, I refer you to the <a href=" https://www.macaulaylibrary.org/how-to/recording-techniques/ ">Macaulay Library</a>. There you will find all you need to know about “Audio recording techniques” and “Audio recording gear,” including up-to-date information on microphones, digital audio recorders, accessories, and smartphones. As of 2019, there are three superb videos by Greg Budney, ex-curator of the Library, telling how to record: The Basics, With Parabola, and With Shotgun Microphone.</p> 
				<p>  <a href="https://wilsonsociety.org/pubs/manual-of-field-and-laboratory-exercises-for-ornithology/">An exercise</a> prepared for the Wilson Ornithological Society also provides information on how to record with smartphones.</p>
				<p>My favorite recording system? I love parabolic microphones, because the highly directional parabolic reflector physically amplifies the sound from a distant source and concentrates it at the microphone, enabling much “cleaner” recordings of distant birds than a shotgun microphone can provide (but see advantages and disadvantages of various microphones on the Cornell website). I use a stereo <a href="http://www.telinga.com/">Telinga microphone</a> in the parabola, with good headphones over the ears (Sennheiser HD 25-1 II). For a recorder, I use a Sound Devices digital recorder (Model 722, an old one!), and my partner Janet Grenzke uses a Marantz PMD661. Our next recorder will probably be a MixPre from Sound Devices.</p>
				<p>When I want long recordings, perhaps even overnight, and when I can set the microphone fairly close to the bird, I use a Marantz PMD661 recorder and an Audio Technica BP4027 stereo shotgun microphone. An external Voltaic battery (Model V44) supplies the power for a session that might last overnight. Sample recordings with this setup include the night-singing mockingbirds (e.g., ♫238, ♫516) and the sage thrasher who sang predictably during the daytime from just a few perches (e.g., ♫518, ♫528).</p>
				<p>When I want to program a recorder to record only at certain times of the day over a week or more, all unattended, and when I will settle for a monaural recording, I use a <a href="https://www.birds.cornell.edu/ccb/swift/">Swift</a>. Sample recordings include those from the night-singing sage thrasher (e.g., ♫519-527, ♫529-531), from the male northern cardinal singing at dawn (♫468-476), and from the female cardinal singing on the nest (♫60).</p>
				
