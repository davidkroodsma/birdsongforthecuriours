				<h2>Additional Resources</h2>
				
				
				<p>1) Check out the exercise prepared for The Wilson Ornithological Society, on “Recording and analysis of bird vocalizations,” by Sylvia L. Halkin and Walter J. Berry, found <a href="https://wilsonsociety.org/pubs/manual-of-field-and-laboratory-exercises-for-ornithology/">here</a>. It offers good “how to” information: How to get the best recordings you can with a smartphone, extensive and helpful notes on how to use Raven Lite for analyzing sounds, etc. Included is a useful glossary of terms (e.g., vocalization, frequency, amplitude, etc.), and a list of questions you might be able to answer about bird sounds, much like my Explore sections.</p>
				<p>2) The Cornell Lab of Ornithology’s <a href="https://academy.allaboutbirds.org/all-about-bird-song/">“All About Birdsong”</a>, With audio and video; a game on bird identification included for 30 species. </p>
				<p>3) On the internet you can find more bird sounds for your listening pleasure and study:</p>
				<ul>
				<li><a href="https://www.xeno-canto.org/">Xeno-canto</a> —  “a website dedicated to sharing bird sounds from all over the world.”</li>		
				<li><a href="https://www.macaulaylibrary.org/">The Cornell Lab of Ornithology’s Macaulay Library</a> — A rapidly growing archive of brief sound clips, as birders attach recordings to their eBird submissions. All recordings can be viewed as sonagrams.</li> 
				<li><a href="https://osuc.biosci.ohio-state.edu/blb/">The Borror Laboratory of Bioacoustics at Ohio State University</a> — “one of the oldest and most extensive . . . collections of recorded animal sounds . . . in the world . . .”</li>
				<li><a href="https://www.floridamuseum.ufl.edu/bird-sounds/ ">The Bird Sounds Database </a> at the Florida Museum of Natural History--sounds from over 3000 species of birds, all available online</li>
				<li><a href="https://sounds.bl.uk/">The British Library</a> — An extensive, eclectic collection of earth sounds, including “Environment and Nature” <l/>
				<li><a href="https://academy.allaboutbirds.org/avian-vocal-behavior-sound-visualizations/"> Avian Vocal Behavior—Sound Visualizations</a>. Sonagrams and accompanying sounds, illustrating Chapter 10 in the Handbook of Bird Biology, 3rd edition, from the Cornell Lab of Ornithology.</li>
				<li><a href="http://listeningtoacontinentsing.com/">The companion website</a> to my book <i>Listening to a Continent Sing: Birdsong by Bicycle from the Atlantic to the Pacific</i>, — a sample of bird sounds from North America, rich in explanation as to what you are hearing.</li> </p>
				
				
				<p>4) The following books or websites provide additional perspectives on birdsong:</p>
				<ul>
					<li>Constantine, M., and The Sound Approach. 2006. The Sound Approach to Birding. A guide to understanding bird sound. The Sound Approach, Poole, Dorset UK. Beautiful sonagrams, with sounds to accompany them. </li>
					<li>Elliott, Lang. <a href="https://musicofnature.com/">The Music of Nature</a> Celebrating the Voices of the Natural World. Elliott is a first-rate sound recordist, of birds, frogs, mammals, insects, and more, and author of many books on bird song (e.g., Common Birds and their Sounds; Music of the Birds—A Celebration of Bird Song).</li> 
					<li>Kroodsma, D. 2005. The Singing Life of Birds. The Art and Science of Listening to Birdsong. Boston, Massachusetts, Houghton-Mifflin Co. Winner of the John Burroughs Medal for natural history writing.</li> 
					<li>Kroodsma, D. 2008. The Backyard Birdsong Guide. Eastern and Central North America, and Western North America. San Francisco, California, Chronicle Books. Has sold nearly half a million copies; suitable for young of all ages, with an electronic module that plays the sounds for each of 75 species. More recently republished by Princeton University Press and the Cornell Lab Publishing Group.</li>
					<li>Kroodsma, D. 2009. Birdsong by the Seasons. A Year of Listening to Birds. Boston, Massachusetts, Houghton-Mifflin Harcourt Co. Twenty-four chapters, exploring bird sounds from January through December.</li>
					<li>Kroodsma, D. 2016. Listening to a Continent Sing. Birdsong by Bicycle from the Atlantic to the Pacific. Princeton, New Jersey, Princeton University Press. A 4500-mile bicycle ride from Virginia to Oregon, with my son David, listening to birds every inch of the way. On <a href="http://listeningtoacontinentsing.com/">the companion website</a> you can choose how to listen to the 381 recordings from this journey (by species, by location, or during the dawn chorus). Explanations on how to listen are provided for each recording, with background species noted.</li>
					<li>Low, T. 2014. Where song began. Australia’s Birds and how they Changed the World. New Haven, Connecticut, Yale University Press. An exciting look at how birds sing so differently “down under.”</li>
					<li>Pieplow, N. 2017. Peterson Field Guide to Bird Sounds of Eastern North America. Houghton Mifflin Harcourt, Boston, Massachusetts. “ . . . the most comprehensive reference to North American bird sounds ever produced . . .”</li>
					<li>Stap, D. 2005. Birdsong: A Natural History. New York City, New York, Charles Scribner's Sons. A “creative mixture of reportage, storytelling, and research, Stap distills the complexities of the study of birdsong and unveils a remarkable discovery that sheds light on the mystery of mysteries . . .” (Amazon)</li>
					<li>Taylor, H. 2017. Is Birdsong Music? Outback Encounters with an Australian Songbird. Bloomington, Indiana, Indiana University Press. From the perspective of a musician.</li>
					<li>Young, J. 2012. What the Robin Knows. How Birds reveal the Secrets of the Natural World. Boston, Massachusetts, Houghton-Mifflin Harcourt Co. “Here is the ancestral wisdom passed down from the Apache elder Stalking Wolf to the renowned tracker Tom Brown to Jon Young himself . . . the art of truly listening to the avian soundscape.”</li>
				</ul>
				
